     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $producto = new Producto();
            $producto->nombre = $request->input('nombre');
        $producto->descripcion = $request->input('descripcion');
        $producto->fecha_caducidad = $request->input('fecha_caducidad');
        $producto->precio_unitario = $request->input('precio_unitario');
        $producto->activo = $request->input('activo');
        $producto->user_id = $request->input('user_id');
        $producto->save();
        return response()->json(
            ['producto' => $producto,
                'msg'=> 'Contrato creado correctamente']);

    }

